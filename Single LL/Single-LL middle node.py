class Node:
    def __init__(self, value):
        self.value = value
        self.next = None


class LinkedList:
    def __init__(self, value):
        new_node = Node(value)
        self.head = new_node
        self.tail = new_node

    def append(self, value):
        new_node = Node(value)
        if self.head == None:
            self.head = new_node
            self.tail = new_node
        else:
            self.tail.next = new_node
            self.tail = new_node
        return True

    def find_middle_node(self):
        temp=self.head
        mid=self.head
        length=0
        if self.head==None:
            return None
        while temp is not None:
            length+=1
            temp=temp.next
        for _ in range(length//2):
            mid=mid.next
        return mid


my_linked_list = LinkedList(1)
my_linked_list.append(2)
